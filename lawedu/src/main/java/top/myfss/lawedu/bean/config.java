package top.myfss.lawedu.bean;

/**
 * 配置
 */
public class config {

    public static String baseDir;
    public static String assignmentDir;
    public static String videoDir;

    public String getBaseDir() {
        return baseDir;
    }

    public void setBaseDir(String baseDir) {
        this.baseDir = baseDir;
    }

    public String getAssignmentDir() {
        return assignmentDir;
    }

    public void setAssignmentDir(String assignmentDir) {
        this.assignmentDir = assignmentDir;
    }

    public String getVideoDir() {
        return videoDir;
    }

    public void setVideoDir(String videoDir) {
        this.videoDir = videoDir;
    }
}
